    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Obtem o model da Table
        DefaultTableModel m = 
                (DefaultTableModel) tabDados.getModel();
        
        // Abre Dialog
        DialogRevisao02 d = new DialogRevisao02(this, true);
        d.setVisible(true);
        
        // Recebe os valores digitados em Form        
        String var1 = d.getTxtCampo1().getText();
        String var2 = d.getTxtCampo2().getText();
        
        // Cria um array com o mesmo numero de colunas da 
        // tabela. posições = colunas
        String[] linha = {var1, var2};
        
        // Adiciona a linha (array) ao modelo
        m.addRow(linha);
   
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selecionado = 
           tabDados.getSelectedRow();
        System.out.println("->" + selecionado);
        
        // Obtem o model da Table
        DefaultTableModel m = 
                (DefaultTableModel) tabDados.getModel();        
        // Remove linha pelo indice
        m.removeRow(selecionado);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tabDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabDadosMouseClicked
        int selecionado = 
           tabDados.getSelectedRow();
        System.out.println("->" + selecionado);

        String colA = 
           tabDados.getValueAt(selecionado, 0).toString();
        System.out.println("---> " + colA);
        
        DialogRevisao02 d = new DialogRevisao02(this, true);
        d.getTxtCampo1().setText(colA);
        d.setVisible(true);
        //txtCampo1.setText(colA);
    }//GEN-LAST:event_tabDadosMouseClicked

